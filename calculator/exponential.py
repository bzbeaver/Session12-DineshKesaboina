import math

def exp(somenum):
    '''
       function to calculate exponential of a number
       Args:
           somenum: int or float
       Returns: exponential of a given number
       '''
    print(f'Exponential is : {math.exp(somenum)}')
    return math.exp(somenum)

def dexp(somenum):
    '''
       function to calculate the derivative of exponential of a number
       Args:
           somenum: int or float
       Returns: derivative of exponential of a given number
       '''
    print(f'Derivative of exponential is: {math.exp(somenum)}')
    return math.exp(somenum)